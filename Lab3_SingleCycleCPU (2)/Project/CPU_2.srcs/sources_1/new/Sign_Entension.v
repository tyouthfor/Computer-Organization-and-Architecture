`timescale 1ns / 1ps

//*********************************************************
//           模块名称：Sign_Extension
//           模块功能：将16位立即数符号扩展成32位
//*********************************************************
module Sign_Extension(
    input [15:0] x,

    output [31:0] y
    );

    assign y = {{16{x[15]}}, x};

endmodule
